#include "libft.h"

char	*ft_strdup(const char *source)
{
	char	*str;
	int		i;

	if (!source)
		return (NULL);
	i = 0;
	while (source[i])
		i++;
	str = malloc(sizeof(char) * i + 1);
	if (str == NULL)
		return (NULL);
	i = 0;
	while (source[i])
	{
		str[i] = source[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
